public abstract class BasicWorker {


    private void wakeUp(){
        System.out.println("wakeUp");
    }

    private void goToHome(){
        System.out.println("go home");
    }

    private void asleep(){
        System.out.println("asleep");
    }

    /**
     * print the name of worker
     */
    public abstract void name();

    /**
     * print the work of worker
     */
    public abstract void work();

    private  void action(Behavior behavior){
        switch (behavior){
            case WAKE_UP:
                wakeUp();
                break;
            case WORK:
                work();
                break;
            case GOTO_HOME:
                goToHome();
                break;
            case ASLEEP:
                asleep();
                break;
                default:
                    //ignore
                    break;
        }
    }

    public void action(Behavior... behaviors){
        for (Behavior b : behaviors){
            action(b);
        }
    }

    static enum Behavior{
        //make a enum
        WAKE_UP,WORK,GOTO_HOME,ASLEEP;
    }


}
