public class ContainerDemo {

    private static Map<String,Object> singletonHolder = new ConcurrentHashMap<>();


    private ContainerDemo(){}

    public static void registerService(String key,Object instance){
        if (!singletonHolder.containsKey(key)){
            singletonHolder.put(key, instance);
        }
    }


    public static Object getSingletonInstance(String key){
        return singletonHolder.get(key);
    }


}
