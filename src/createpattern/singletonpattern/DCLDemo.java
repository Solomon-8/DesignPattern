public class DCLDemo {
    private volatile static DCLDemo dclDemo;

    private DCLDemo(){}


    public static DCLDemo getInstance(){
        if (dclDemo == null){
            synchronized (DCLDemo.class){
                if (dclDemo == null){
                    dclDemo = new DCLDemo();
                }
            }
        }
        return dclDemo;
    }

}
