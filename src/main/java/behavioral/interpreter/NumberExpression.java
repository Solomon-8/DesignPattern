public class NumberExpression implements Expression{

    private int num;


    public NumberExpression(int num) {
        this.num = num;
    }

    public NumberExpression(String num) {
        this.num = Integer.parseInt(num);
    }

    @Override
    public int interpret() {
        return num;
    }
}
