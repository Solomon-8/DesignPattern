public class Wizard {

    private Logger logger = LoggerFactory.getLogger(Wizard.class);

    private Deque<Command> undoDeque = new LinkedList<>();

    private Deque<Command> redoDeque = new LinkedList<>();


    public void castSpell(Command command, Target target) {
        logger.info("{} casts {} at {}", this, command, target);
        command.execute(target);
        undoDeque.offerLast(command);
    }

    public void undoLastSpell() {
        if (!undoDeque.isEmpty()) {
            Command previousSpell = undoDeque.pollLast();
            redoDeque.offerLast(previousSpell);
            logger.info("{} undoes {}", this, previousSpell);
            previousSpell.undo();
        }
    }

    public void redoLastSpell() {
        if (!redoDeque.isEmpty()) {
            Command previousSpell = redoDeque.pollLast();
            undoDeque.offerLast(previousSpell);
            logger.info("{} redoes {}", this, previousSpell);
            previousSpell.redo();
        }
    }


}
