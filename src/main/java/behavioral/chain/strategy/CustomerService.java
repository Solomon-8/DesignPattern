public class CustomerService {

    private CustomerService next;
    private Handler handler;

    public CustomerService(Handler handler) {
        this.handler = handler;
    }

    public CustomerService getNext() {
        return next;
    }

    public void setNext(CustomerService next) {
        this.next = next;
    }

    public Handler getHandler() {
        return handler;
    }

    public void setHandler(Handler handler) {
        this.handler = handler;
    }

    public void handler(String task){
        if (handler.canHandler(task)){
            handler.handler();
        } else if (next != null){
            next.handler(task);
        } else {
            System.out.println("can't help you may need to charge more money.");
        }
    }
}
