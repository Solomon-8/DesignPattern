public class MyIterator implements Iterator {


    private MyItems items;
    private int idx;


    public MyIterator(MyItems items) {
        this.items = items;
        this.idx = -1;
    }

    @Override
    public boolean hasNext() {
        return findNextIndex() != -1;
    }

    public int findNextIndex(){
        List<Item> content = items.getItems();
        int tempIdx = idx;
        if (idx >= content.size() -  1){
            tempIdx = -1;
        } else {
            tempIdx++;
        }
        return tempIdx;
    }

    @Override
    public Object next() {
        idx = findNextIndex();
        if (idx != -1){
            return items.getItems().get(idx);
        }
        return null;
    }


}
