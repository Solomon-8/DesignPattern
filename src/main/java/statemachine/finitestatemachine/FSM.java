public class FSM {
    private final Map<StateEnum, AbstractState> container = new ConcurrentHashMap<>(15);
    private StateEnum curState = StateEnum.IDLE;


    public void tick(){
        container.get(curState).onStateTick();
    }

    public boolean register(StateEnum stateNum, AbstractState state){
        container.put(stateNum,state);
        return true;
    }


    public boolean trans(StateEnum state){
        container.get(curState).onStateExit();
        container.get(state).onStateEnter();
        curState = state;
        return true;
    }

    public boolean setInitState(StateEnum state){
        this.curState = state;
        return true;
    }

}
