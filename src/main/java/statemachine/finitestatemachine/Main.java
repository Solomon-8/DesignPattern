public class Main {
    public static void main(String[] args) throws InterruptedException {
        FSM machine = new FSM();
        IdleState idleState = new IdleState(machine);
        AttackState attackState = new AttackState(machine);
        BackState backState = new BackState(machine);
        machine.setInitState(StateEnum.IDLE);
        machine.register(StateEnum.IDLE,idleState);
        machine.register(StateEnum.ATTACK,attackState);
        machine.register(StateEnum.BACK,backState);
        while (true){
            machine.tick();
            Thread.sleep(2000);
        }
    }
}
