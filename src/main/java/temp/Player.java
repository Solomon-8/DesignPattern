public class Player {

    private Socket socket;
    private String name;
    private int playerId;

    private List<InfluenceCard> cards = new ArrayList<>();

    public Player(Socket socket, String name, int playerId) {
        this.socket = socket;
        this.name = name;
        this.playerId = playerId;
        this.cards.add(InfluenceCard.DOUBLE);
        this.cards.add(InfluenceCard.FREEDOM);
        this.cards.add(InfluenceCard.REPLACEMENT);
    }

    public Socket getSocket() {
        return socket;
    }

    public void setSocket(Socket socket) {
        this.socket = socket;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPlayerId() {
        return playerId;
    }

    public void setPlayerId(int playerId) {
        this.playerId = playerId;
    }

    public List<InfluenceCard> getCards() {
        return cards;
    }

    public void setCards(List<InfluenceCard> cards) {
        this.cards = cards;
    }
}
